# Создаем матрицу
matrix = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]

# Выводим матрицу на экран
print("Матрица:")
for row in matrix:
    print(row)

# Запрашиваем у пользователя номер строки
n = int(input("Введите номер строки: "))

# Проверяем, что номер строки находится в допустимых пределах
if n < 1 or n > len(matrix):
    print("Некорректный номер строки")
else:
    # Находим сумму и произведение элементов строки N
    row_sum = sum(matrix[n-1])
    row_product = 1
    for elem in matrix[n-1]:
        row_product *= elem

    # Выводим результаты на экран
    print("Сумма элементов строки {} равна {}".format(n, row_sum))
    print("Произведение элементов строки {} равно {}".format(n, row_product))
